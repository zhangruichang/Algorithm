class Solution {
public:
    static bool comp(pair<int,bool> p1, pair<int,bool> p2){
        if(p1.fi!=p2.fi) return p1.fi<p2.fi;
        else return p1.se>p2.se;
    }
    int minMeetingRooms(vector<Interval>& in) {
        vector<pair<int, bool>> v;
        for(auto e: in){
            v.push_back({e.start, 0});
            v.push_back({e.end, 1});
        }
        sort(v.begin(), v.end(), comp);
        for(auto e: v){
          cout<<e.fi<<" "<<e.se<<endl;
        }
        int maxans=0, ans=0;
        for(auto e: v){
            if(!e.se){
                ans++;
            }else{
                ans--;
            }
            maxans=max(maxans, ans);
        }
        return maxans;
    }
} S;

